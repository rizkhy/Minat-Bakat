<h2>Tentang Kami</h2>

<br><br>
<p>	Di era globalisasi teknologi saat ini, kebutuhan informasi dalam dunia pendidikan menjadi sangat penting dalam menentukan kemajuan suatu kelompok. Sistem Informasi Minat dan bakat ini dibuat untuk membantu Masyarakat khususnya wilayah Yogyakarta untuk berbagi ilmu dengan menyalurkan minat dan bakatnya secara online sehingga mudah di akses oleh siapa saja, dengan pemanfaatan dan penerapan teknologi informasi, kumpulan minat dan bakat dapat tersusun rapi di dalam sebuah sistem informasi sehingga mempermudah siapapun untuk mengaksesnya </p>